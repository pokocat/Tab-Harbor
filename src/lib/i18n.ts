export interface I18nApi {
  locale: SupportedLocale;
  t: (key: string, params?: Record<string, string | number>) => string;
  pick: (key: string) => string;
}

export function resolveLocale(preference: LocalePreference, browserLocale = chrome.i18n.getUILanguage()): SupportedLocale {
  if (preference === "en" || preference === "zh-CN") {
    return preference;
  }

  return normalizeBrowserLocale(browserLocale);
}

export function normalizeBrowserLocale(locale: string): SupportedLocale {
  return locale.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function createI18n(preference: LocalePreference, browserLocale?: string): I18nApi {
  const locale = resolveLocale(preference, browserLocale);

  function t(key: string, params: Record<string, string | number> = {}): string {
    const value = messages[locale][key] ?? messages.en[key] ?? key;
    return value.replace(/\{(\w+)\}/g, (_match: string, token: string) => String(params[token] ?? ""));
  }

  function pick(key: string): string {
    return t(key);
  }

  return { locale, t, pick };
}
